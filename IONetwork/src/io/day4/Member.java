package io.day4;

import java.io.Serializable;

public class Member implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String name;
	private String passwd;
	private String id;
	private String age;
	
	public Member() {}
	
	public Member(String id, String passwd, String name, String age) {
		super();
		this.name = name;
		this.passwd = passwd;
		this.id = id;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}
	
	@Override
	public String toString() {
		return ">> 아이디 :"+id+
				"\n>> 암호 :"+passwd+
				"\n>> 이름 :"+name+
				"\n>> 나이 :"+age+"\n";
		
	}
	
}
